
/* Drop Tables */

DROP TABLE ORDERS CASCADE CONSTRAINTS;
DROP TABLE BOOK CASCADE CONSTRAINTS;
DROP TABLE COSTOMER CASCADE CONSTRAINTS;




/* Create Tables */

CREATE TABLE BOOK
(
	BOOKID varchar2(4),
	BOOKNAME varchar2(20) NOT NULL,
	PUBLISHER varchar2(20) NOT NULL,
	PRICE number(10) NOT NULL,
	PRIMARY KEY (BOOKID)
);


CREATE TABLE COSTOMER
(
	CUSTID varchar2(3),
	NAME varchar2(10) NOT NULL,
	ADRESS varchar2(20) NOT NULL,
	PHONE varchar2(15),
	PRIMARY KEY (CUSTID)
);


CREATE TABLE ORDERS
(
	ORDERID varchar2(5) NOT NULL,
	CUSTID varchar2(3) NOT NULL,
	BOOKID varchar2(4) NOT NULL,
	SALEPRICE number(10),
	ORDERDATE varchar2(10),
	PRIMARY KEY (ORDERID)
);



/* Create Foreign Keys */

ALTER TABLE ORDERS
	ADD FOREIGN KEY (BOOKID)
	REFERENCES BOOK (BOOKID)
;


ALTER TABLE ORDERS
	ADD FOREIGN KEY (CUSTID)
	REFERENCES COSTOMER (CUSTID)
;



